#ifndef __MYIIC_H
#define __MYIIC_H
#include "sys.h"
#include "utility.h"



//IO方向设置
#define BME_SDA_IN()  {GPIOB->CRH&=0XFFFF0FFF;GPIOB->CRH|=8<<12;}
#define BME_SDA_OUT() {GPIOB->CRH&=0XFFFF0FFF;GPIOB->CRH|=3<<12;}

//IO操作函数
#define BME_IIC_SCL    PBout(10) //SCL
#define BME_IIC_SDA    PBout(11) //SDA	 
#define BME_READ_SDA   PBin(11)  //输入SDA 

//IIC所有操作函数
void BME_IIC_Init(void);                //初始化IIC的IO口
void BME_IIC_Start(void);				//发送IIC开始信号
void BME_IIC_Stop(void);	  			//发送IIC停止信号
void BME_IIC_Send_Byte(u8 txd);			//IIC发送一个字节
u8 BME_IIC_Read_Byte(unsigned char ack);//IIC读取一个字节
u8 BME_IIC_Wait_Ack(void); 				//IIC等待ACK信号
void BME_IIC_Ack(void);					//IIC发送ACK信号
void BME_IIC_NAck(void);				//IIC不发送ACK信号

void BME_IIC_Write_One_Byte(u8 daddr,u8 addr,u8 data);
u8 BME_IIC_Read_One_Byte(u8 daddr,u8 addr);
#endif
















